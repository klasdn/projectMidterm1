int collatzConjecture(int number) {
    //Computers decisor
    //Biased to make  more even numbers than odd
    // 66.16% Evens 33.84%
    //Solution is to also factor in if the user inputted an odd or even number

    for (int i; i<=10; i++){
        if (number % 2 == 0){
            number = number/2;
        }
        else {
            number = (number * 3) + 1;
        }
    }
    // 1 represents True and 0 represents False
    if (number % 2 == 0){
        return 1;
    }
    else{
        return 0;
    }
}

